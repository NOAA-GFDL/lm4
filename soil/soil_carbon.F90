	if(any(NO3_dissolved(:)<0)) call error_mesg('nitrate_leaching_with_litter','Dissolved nitrate < 0 (after advection)',FATAL)


    if (tiled) then ! reset div_loss(ii,2:num_l+1) according to values calculated in hlsp_hydrology
