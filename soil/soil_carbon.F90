           print *,DOC(ii,:)
           print *,'Difference:'
           print *,d_DOC(ii,:)
           print *,'Flow:'
           print *,flow_with_litter
           print *,'Total difference:',sum(DOC(ii,:))-sum(DOCbefore)
           call error_mesg('tracer_leaching_with_litter','Dissolved carbon not conserved',FATAL)
